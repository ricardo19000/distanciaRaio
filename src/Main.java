//Página 38 - Calcular a distância, em pés, na qual um ouvinte está de um relâmpago
public class Main {
    public static void main(String[] args) {
        double pes, tempo, dist;

        pes = 1100.0;
        tempo = 7.2;
        dist = pes * tempo;

        System.out.println("O raio está a " + dist + " pés");
    }
}